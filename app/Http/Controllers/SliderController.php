    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
      
			// Upload media
			// dd($request->isFree);
			$imageName = $this->uploadImage($request->filex, 'assets/sliders');
           
            $request['file'] = 'assets/sliders/'. $imageName;
            Slider::create([
                'title' => $request->title,
                'file' => $request['file'],
            ]);
	

        return redirect(route('slider.index'))->with('message', 'Slider added');
    }

